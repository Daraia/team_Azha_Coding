package week1_javaCodingTask;

public class evenOrOdd {

    public static void main(String[] args) {

        int a = 9;
        int b = 10;

        if(a % 2 == 0){
            System.out.println("It is an even number");

        }else {
            System.out.println("It is a odd number");
        }

        if(b % 2 == 0){
            System.out.println("It is an even number");

        }else{
            System.out.println("It is a odd number");        }
    }
}
